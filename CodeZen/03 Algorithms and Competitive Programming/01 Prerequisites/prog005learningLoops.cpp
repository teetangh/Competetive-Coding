#include <bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[])
{
    freopen("xinput.txt", "r", stdin);
    freopen("xoutput.txt", "w", stdout);

    int n;
    cin >> n;
    int i = 1;
    while (i <= n)
    {
        cout << i << endl;
        i++;
    }

    for (int i = 1; i <= n; i++)
    {
        cout << i << endl;
    }

    return 0;
}
